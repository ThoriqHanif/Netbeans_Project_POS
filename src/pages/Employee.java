    private void AutoID() {
        try {
            String sql = "SELECT MAX(RIGHT (id,4)) AS id_auto FROM employee";
            Statement s = db.conn().createStatement();
            ResultSet rs = s.executeQuery(sql);

            if (rs.next()) {
                String id_auto, nol_plus;
                int p;
                id_auto = Integer.toString(rs.getInt(1) + 1);
                p = id_auto.length();
                nol_plus = "";
                for (int i = 1; i <= 4 - p; i++) {
                    nol_plus = nol_plus + "0";
                }
                txtID.setText("EMP" + nol_plus + id_auto);
            }
        } catch (Exception e) {
            txtID.setText("EMP0001");

        }
    }

