    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        String nama = txtNama.getText();
        String telp = txtTelp.getText();
        String alamat = txtAlamat.getText();

        if (txtNama.getText().equals("") || txtTelp.getText().equals("") || txtAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Data tidak boleh kosong");
        } else {

            try {
                Statement s = db.conn().createStatement();
                s.executeUpdate(" INSERT INTO employee (id,nama, telp, alamat) VALUES (' " + id + "',  ' " + nama + " ', ' " + telp + " ', ' " + alamat + " ') ");
                AutoID();
                Clear();
                JOptionPane.showMessageDialog(rootPane, "Berhasil Menambah data");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
            load_table();
        }

    }//GEN-LAST:event_btnSaveActionPerformed

