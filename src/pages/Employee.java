    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String search = cariID.getText();

        try {
            Statement s = db.conn().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM employee WHERE id = ' " + search + " ' ");

            if (rs.next()) {
                txtID.setText(rs.getString("id"));
                txtNama.setText(rs.getString("nama"));
                txtTelp.setText(rs.getString("telp"));
                txtAlamat.setText(rs.getString("alamat"));
                btnSave.setEnabled(false);
                btnUpdate.setEnabled(true);
                btnDelete.setEnabled(true);

            } else {
                JOptionPane.showMessageDialog(rootPane, "Tidak menemukan data dengan Employee ID " + cariID.getText());
                btnSave.setEnabled(true);
                btnUpdate.setEnabled(false);
                btnDelete.setEnabled(false);
                Clear();

            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        // TODO add your handling code here:
        String nama = txtCari.getText();

        try {
            DefaultTableModel dt = (DefaultTableModel) tblEmployee.getModel();
            dt.setRowCount(0);
            Statement s = db.conn().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM employee WHERE nama = ' " + nama + " ' ");

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));

                dt.addRow(v);

            }
        } catch (Exception e) {
            load_table();
        }
    }//GEN-LAST:event_txtCariKeyReleased

